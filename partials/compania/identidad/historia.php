    const timelineDecades = document.querySelectorAll('.timeline-decade');

    timelineDecades.forEach((decade) => {
        // Select all buttons within the decade
        const buttons = decade.querySelectorAll('button[data-timeline-marker=""]');

        buttons.forEach((button) => {
            // Calculate the percentage based on the data-year attribute within each decade
            const year = parseInt(button.getAttribute('data-year'), 10);
            const startYear = parseInt(decade.querySelector('.decade-label').textContent, 10);
            const endYear = startYear + 10;

            const percentage = ((year - startYear) / 10) * 90; // Adjusted to 90% within each decade

            // Set the left style property of the button
            button.style.left = `${percentage}%`;
        });
    });

    var timelineButtons = document.querySelectorAll('.timeline-indicator li button');

    // Loop through each button and attach a click event listener
    timelineButtons.forEach(function(button) {
        button.addEventListener('click', function() {
            // Get the year associated with the clicked button
            var year = this.getAttribute('data-year');

            // Find the index of the slide with the corresponding year
            var slideIndex = Array.from(document.querySelectorAll('.swiper-slide-history')).findIndex(function(slide) {
                return slide.getAttribute('data-year') === year;
            });

            // If slideIndex is found, slide to that index
            if (slideIndex !== -1) {
                swiperHistory.slideTo(slideIndex); // Assuming 'swiper' is your Swiper instance
            }
        });
    });

</script>

